package de.unisb.cs.st.javalanche.mutation.javaagent.classFileTransfomer;

import java.io.PrintWriter;
import java.lang.instrument.ClassFileTransformer;
import java.lang.instrument.IllegalClassFormatException;
import java.security.ProtectionDomain;

import org.objectweb.asm.ClassReader;
import org.objectweb.asm.ClassVisitor;
import org.objectweb.asm.ClassWriter;
import org.objectweb.asm.util.TraceClassVisitor;

import de.unisb.cs.st.javalanche.mutation.javaagent.MutationPreMain;

public class PrintTransformer implements ClassFileTransformer {

	@Override
	public byte[] transform(ClassLoader loader, String className,
			Class<?> classBeingRedefined, ProtectionDomain protectionDomain,
			byte[] classfileBuffer) throws IllegalClassFormatException {

		ClassWriter cw = new ClassWriter(ClassWriter.COMPUTE_MAXS);

		ClassVisitor cv = cw;
		cv = new TraceClassVisitor(cv, new PrintWriter(MutationPreMain.sysout));

		ClassReader cr = new ClassReader(classfileBuffer);
		cr.accept(cv, ClassReader.EXPAND_FRAMES);
		return classfileBuffer;
	}

}
